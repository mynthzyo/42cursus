int	ft_isalnum(int letter)
{
	if ((letter < '0' || letter > '9')
		&& (letter < 'A' || letter > 'Z')
		&& (letter < 'a' || letter > 'z'))
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
